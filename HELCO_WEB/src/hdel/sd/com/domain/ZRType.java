package hdel.sd.com.domain;

public class ZRType {
	
	private String zgtype;		// 기종
	private String auart;		// 판매문서유형
	private String matnr;		// 자재번호
	private String spart;		// 제품군
	private String zprdgbn;		// 제품구분
	
	public String getZgtype() {
		return zgtype;
	}
	public void setZgtype(String zgtype) {
		this.zgtype = zgtype;
	}
	public String getAuart() {
		return auart;
	}
	public void setAuart(String auart) {
		this.auart = auart;
	}
	public String getMatnr() {
		return matnr;
	}
	public void setMatnr(String matnr) {
		this.matnr = matnr;
	}
	public String getSpart() {
		return spart;
	}
	public void setSpart(String spart) {
		this.spart = spart;
	}
	public String getZprdgbn() {
		return zprdgbn;
	}
	public void setZprdgbn(String zprdgbn) {
		this.zprdgbn = zprdgbn;
	}
	
}
